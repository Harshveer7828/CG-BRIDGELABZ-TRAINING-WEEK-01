public class KilometersToMiles{
	public static void main(String[] args){
		// Creating the variable containing kilometer in distance
		double distanceInkilometers = 10.8;
		// Calculating distance in miles
		double distanceInMiles = 10.8 * 0.621371;
		// Display the distance in miles
		System.out.print(" The distance "+ distanceInkilometers + "Km in miles is "+ distanceInMiles );
	}
	
}